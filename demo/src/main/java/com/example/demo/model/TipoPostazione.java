package com.example.demo.model;

/**
 * Enumerazione che definisce i possibili tipi di postazione disponibili nel sistema.
 */
public enum TipoPostazione {
    /**
     * Postazione singola ad uso esclusivo di una persona
     */
    PRIVATO,
    
    /**
     * Postazione in uno spazio condiviso con altre persone
     */
    OPENSPACE,
    
    /**
     * Sala per riunioni e meeting di gruppo
     */
    SALA_RIUNIONI
} 